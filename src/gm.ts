export function gmProgramToSoundfontName(program: number): string {
  const idx = Math.trunc(program);
  if (idx >= 0 && idx < GM_PROGRAM_TO_SOUNDFONT_NAME.length) {
    return GM_PROGRAM_TO_SOUNDFONT_NAME[idx];
  }
  return "acoustic_grand_piano";
}

// GM channel 10 (index 9) is drums/percussion. Soundfont sets differ in naming
// for percussion; we try a reasonable default that exists in common sets.
export function gmDrumInstrumentName(): string {
  // FluidR3_GM and many midi-js-soundfonts sets include this.
  return "synth_drum";
}
